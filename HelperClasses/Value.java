public class Value {

	double open;
	double close;
	double volume;
	
	Value(double inputOpen, double inputClose, double inputVolume) {
	// holds value of a given stock on a specific date
	// instance variables for day opening price, closing price, volume of stocks available
	
		open = inputOpen;
		close = inputClose;
		volume = inputVolume;
		
	}
}
